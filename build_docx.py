def add_heading(doc, text, level=1):
    h = doc.add_heading(text, level=level)
    if level == 1:
        for run in h.runs:
            run.font.color.rgb = RGBColor(0x1a, 0x1a, 0x2e)
    return h


def add_quote(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.left_indent = Cm(1)
    p.paragraph_format.space_before = Pt(4)
    p.paragraph_format.space_after = Pt(4)
    # Левая граница
    pPr = p._p.get_or_add_pPr()
    pBdr = OxmlElement('w:pBdr')
    left = OxmlElement('w:left')
    left.set(qn('w:val'), 'single')
    left.set(qn('w:sz'), '6')
    left.set(qn('w:space'), '12')
    left.set(qn('w:color'), '4472C4')
    pBdr.append(left)
    pPr.append(pBdr)
    run = p.add_run(text)
    run.italic = True
    run.font.color.rgb = RGBColor(0x33, 0x33, 0x66)
    run.font.size = Pt(11)
    return p


def build_docx():
    doc = Document()

    # Поля страницы
    for section in doc.sections:
        section.top_margin = Cm(2)
        section.bottom_margin = Cm(2)
        section.left_margin = Cm(2.5)
        section.right_margin = Cm(2.5)

    # Титул
    title = doc.add_heading("Кастдев-анализ: голос клиента", 0)
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    title.runs[0].font.color.rgb = RGBColor(0x1a, 0x1a, 0x2e)

    sub = doc.add_paragraph("Массажист Константин · Санкт-Петербург · 15 интервью")
    sub.alignment = WD_ALIGN_PARAGRAPH.CENTER
    sub.runs[0].font.color.rgb = RGBColor(0x88, 0x88, 0x88)
    sub.runs[0].font.size = Pt(11)

    doc.add_paragraph()

    # Парсим секции
    for line in ANALYSIS.strip().splitlines():
        s = line.strip()
        if not s:
            doc.add_paragraph()
            continue

        if s.startswith("## "):
            doc.add_page_break() if s.startswith("## 2.") or s.startswith("## 5.") or s.startswith("## 7.") or s.startswith("## 9.") else None
            add_heading(doc, s[3:], level=1)

        elif s.startswith("**") and s.endswith("**"):
            p = doc.add_paragraph()
            run = p.add_run(s.strip("*"))
            run.bold = True
            run.font.size = Pt(12)

        elif s.startswith("> "):
            add_quote(doc, s[2:])

        elif s.startswith("- "):
            p = doc.add_paragraph(style="List Bullet")
            text = s[2:]
            # Жирный текст до тире если есть
            if "**" in text:
                parts = text.split("**")
                for i, part in enumerate(parts):
                    run = p.add_run(part)
                    if i % 2 == 1:
                        run.bold = True
            else:
                p.add_run(text)

        elif s == "---":
            doc.add_paragraph()
            p = doc.add_paragraph()
            p.paragraph_format.space_after = Pt(0)
            run = p.add_run("─" * 60)
            run.font.color.rgb = RGBColor(0xcc, 0xcc, 0xcc)
            run.font.size = Pt(9)
            doc.add_paragraph()

        else:
            # Обычный текст, обрабатываем **bold**
            p = doc.add_paragraph()
            if "**" in s:
                parts = s.split("**")
                for i, part in enumerate(parts):
                    run = p.add_run(part)
                    if i % 2 == 1:
                        run.bold = True
            else:
                p.add_run(s)

    doc.save(OUTPUT)
    print(f"✅ Сохранено: {OUTPUT}")
    print(f"   Размер: {OUTPUT.stat().st_size // 1024} KB")


if __name__ == "__main__":
    build_docx()
